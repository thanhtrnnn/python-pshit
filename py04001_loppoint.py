class Point:
    def __init__(self, a, b) -> None:
        self.x = a
        self.y = b

    def distance(self, b):
        dis = math.sqrt(pow(self.x - b.x, 2) + pow(self.y - b.y, 2))
        return f'{dis:.4f}'
    
